# Напишіть програму для аналізу фідбеку від відвідувачів курорту «Морська зірка», яка повинна знаходити згадки про меню,
# спортзал, обслуговування(за кожне знайдене співпадіння нараховується 5% знижки на наступне відвідування). Якщо фідбек
# перевищив 60 символів, відвідувачу надається додаткова знижка 15% на наступне відвідування.


def analyze_feedback(feedback):
    discount = 0

    # Перевірка згадок про меню, спортзал та обслуговування
    if "меню" in feedback:
        discount += 5
    if "спортзал" in feedback:
        discount += 5
    if "обслуговування" in feedback:
        discount += 5

    # Перевірка довжини фідбеку
    if len(feedback) > 60:
        discount += 15

    return discount


# Введення фідбеку від відвідувача
feedback = input("Будь ласка, введіть свій фідбек: ")

# Аналіз фідбеку
discount = analyze_feedback(feedback)

# Виведення результатів
if discount > 0:
    print("Вам надається знижка {}% на наступне відвідування!".format(discount))
else:
    print(
        "На жаль, ваш фідбек не містить згадок про меню, спортзал або обслуговування."
    )
